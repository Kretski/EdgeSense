class DummyNN(nn.Module):
    def __init__(self):
        super().__init__()
        self.fc = nn.Linear(3, 1) # 3 сензора
    def forward(self, x):
        return self.fc(x)

model = type('obj', (object,), {'nn_model': DummyNN()})

