# === 2. Експорт към MISRA-C (Същият железен стандарт) ===
def export_to_misra_c(equation_str, gravity_val=0.015, model_name="real_physics_model"):
    misra_eq = re.sub(r'(?<![a-zA-Z])(\d+\.\d+)', r'\1f', equation_str)
    misra_eq = misra_eq.replace("sin", "sinf").replace("cos", "cosf")
    misra_eq = misra_eq.replace("sqrt", "sqrtf").replace("exp", "expf")

    c_header = f"""/* * GENERATED BY EDGESENSE NANO v2.0 (REAL DATA)
 * Synergy: Discovered Physical Law + Gravitational Feedback
 */
#include <stdint.h>
#include <math.h>

typedef float float32_t;
#define GRAV_G {gravity_val}f

float32_t {model_name}_inference(float32_t x0, float32_t x1, float32_t x2) {{
    /* Откритият закон за управление (на база реални сензори) */
    float32_t azuro_out = {misra_eq};
    
    /* Синергийна стабилизация */
    float32_t synergy_force = -GRAV_G * azuro_out;
    
    return azuro_out + synergy_force;
}}
"""
    return c_header

